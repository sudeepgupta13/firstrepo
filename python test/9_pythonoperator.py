# Arithmetic Operators in Python    
a = 10
b = 3   
# Addition
add = a + b 
print("Addition:", add)  # Output: 13

# Subtraction
sub = a - b
print("Subtraction:", sub)  # Output: 7

# Multiplication
mul = a * b 
print("Multiplication:", mul)  # Output: 30

# Division
div = a / b 
print("Division:", div)  # Output: 3.3333333333333335

# Floor Division
floor_div = a // b  
print("Floor Division:", floor_div)  # Output: 3

# Modulus
mod = a % b 
print("Modulus:", mod)  # Output: 1

# Exponentiation
exp = a ** b 
print("Exponentiation:", exp)  # Output: 1000   

# Assignment Operators in Python
x = 5   
print("Initial value of x:", x)  # Output: 5
x += 3  
print("After x += 3:", x)  # Output: 8  
x -= 2  
print("After x -= 2:", x)  # Output: 6
x *= 4
print("After x *= 4:", x)  # Output: 24
x /= 6
print("After x /= 6:", x)  # Output: 4.0
x //= 2
print("After x //= 2:", x)  # Output: 2.0   
x %= 2
print("After x %= 2:", x)  # Output: 0.0
x **= 3
print("After x **= 3:", x)  # Output: 0.0

#ccomparison Operators in Python
x = 10
y = 20  
print("x == y:", x == y)  # Output: False
print("x != y:", x != y)  # Output: True
print("x > y:", x > y)    # Output: False
print("x < y:", x < y)    # Output: True    
print("x >= y:", x >= y)  # Output: False
print("x <= y:", x <= y)  # Output: True

# logical Operators in Python
p = True
q = False
print("p and q:", p and q)  # Output: False
print("p or q:", p or q)    # Output: True  
print("not p:", not p)      # Output: False
print("not q:", not q)      # Output: True  

x = 5
print("x > 3 and x < 10:", x > 3 and x < 10)  # Output: True
print("x < 3 or x < 10:", x < 3 or x < 10)    # Output: True    
print("not (x > 3):", not (x > 3))            # Output: False

